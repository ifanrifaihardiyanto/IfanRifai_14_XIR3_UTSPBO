    private void selectData() {
        String kolom[] = {"NamaPemesan","NOID","StationAwal","StationTujuan","TanggalKeberangkatan","JenisKereta","WaktuBerangkat","TempatDuduk","Harga"};
                DefaultTableModel dtm = new DefaultTableModel(null, kolom);
                String SQL = "SELECT * FROM tb_data";
                ResultSet rs = KoneksiDB.executeQuery(SQL);
                try{
                    while(rs.next()){
                        String NamaPemesan = rs.getString(1);
                        String NOID = rs.getString(2);
                        String StationAwal = rs.getString(3);
                        String StationTujuan = rs.getString(4);
                        String TanggalKeberangkatan = rs.getString(5);
                        String JenisKereta = rs.getString(6);
                        String WaktuBerangkat = rs.getString(7);
                        String TempatDuduk = rs.getString(8);
                        String Harga = rs.getString(9);
                        String data[] = {NamaPemesan,NOID,StationAwal,StationTujuan,TanggalKeberangkatan,JenisKereta,WaktuBerangkat,TempatDuduk,Harga};
                        dtm.addRow(data);
                    }
                }catch (SQLException ex){
                    Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE,null,ex);
                }
                tb1Data.setModel(dtm);
    }
