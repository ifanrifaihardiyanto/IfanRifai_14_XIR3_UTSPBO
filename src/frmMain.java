    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(Tanggal.getDate()); 
        
        if (etNama.getText().equals("") || etID.getText().equals("") || etStAwal.getText().equals("") || etStTujuan.getText().equals("") ||
            tanggal.equals("") || jKereta.getSelectedItem().equals("") || etWaktu.getText().equals("") || jDuduk.getSelectedItem().equals("") || etHarga.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
        String SQL = "INSERT INTO tb_data (NamaPemesan,NOID,StationAwal,StationTujuan,TanggalKeberangkatan,JenisKereta,WaktuBerangkat,TempatDuduk,Harga)"
                + "VALUES ('"+etNama.getText()+"','"+etID.getText()+"','"+etStAwal.getText()+"','"+etStTujuan.getText()+"','"+tanggal+"','"+jKereta.getSelectedItem()+"','"+etWaktu.getText()+"','"+jDuduk.getSelectedItem()+"','"+etHarga.getText()+"')";
        int status = KoneksiDB.execute(SQL);
        if(status == 1){
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            selectData();
        }else {
            JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
        }
        }
    }//GEN-LAST:event_BtnSaveActionPerformed

